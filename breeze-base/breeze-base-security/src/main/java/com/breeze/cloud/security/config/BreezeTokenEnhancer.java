package com.breeze.cloud.security.config;

import com.breeze.cloud.security.domain.BreezeLoginUser;
import org.springframework.security.oauth2.common.DefaultOAuth2AccessToken;
import org.springframework.security.oauth2.common.OAuth2AccessToken;
import org.springframework.security.oauth2.provider.OAuth2Authentication;
import org.springframework.security.oauth2.provider.token.TokenEnhancer;

import java.util.HashMap;
import java.util.Map;

/**
 * @author breeze
 * @date 2022-02-27
 **/
public class BreezeTokenEnhancer implements TokenEnhancer {

    @Override
    public OAuth2AccessToken enhance(OAuth2AccessToken accessToken, OAuth2Authentication authentication) {
        Map<String, Object> additionalInfo = new HashMap<>();
        BreezeLoginUser breezeLoginUser = (BreezeLoginUser) authentication.getUserAuthentication().getPrincipal();
        additionalInfo.put("breezeLoginUser", breezeLoginUser);
        ((DefaultOAuth2AccessToken) accessToken).setAdditionalInformation(additionalInfo);
        return accessToken;
    }

}
